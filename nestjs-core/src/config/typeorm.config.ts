import { ConfigModule, ConfigService } from '@nestjs/config';
import {
  TypeOrmModuleAsyncOptions,
  TypeOrmModuleOptions,
} from '@nestjs/typeorm';
import { Menu } from 'src/entity/menu.entity';
import { Permission } from 'src/entity/permission.entity';
import { RoleMenu } from 'src/entity/role-menu.entity';
import { RolePermission } from 'src/entity/role-permission.entity';
import { Role } from 'src/entity/role.entity';

export const typeOrmAsyncConfig: TypeOrmModuleAsyncOptions = {
  imports: [ConfigModule],
  inject: [ConfigService],
  useFactory: async (): Promise<TypeOrmModuleOptions> => {
    return {
      type: 'mysql',
      host: process.env.DB_HOST,
      port: parseInt(process.env.DB_PORT, 10),
      username: process.env.DB_USERNAME,
      database: process.env.DB_NAME,
      password: process.env.DB_PASSWORD,
      entities: [Menu,Role,Permission,RolePermission,RoleMenu],
      synchronize: true,
    };
  },
};

